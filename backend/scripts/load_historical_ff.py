async def main():
    conn = await asyncpg.connect(os.environ["DATABASE_URL"])
    try:
        inserted = updated = 0
        for date_str, value in HISTORICAL_FF:
            d = date.fromisoformat(date_str)
            tag = await conn.execute(
                """
                INSERT INTO foreign_flow_daily (date, net_idr_billions, source, fetched_at)
                VALUES ($1, $2, 'historical_backfill', NOW())
                ON CONFLICT (date) DO UPDATE
                  SET net_idr_billions = EXCLUDED.net_idr_billions,
                      source = EXCLUDED.source,
                      fetched_at = NOW()
                  WHERE foreign_flow_daily.source = 'historical_backfill'
                """,
                d, value,
            )
            if "INSERT 0 1" in tag:
                inserted += 1
            elif "UPDATE 1" in tag:
                updated += 1

        # Don't overwrite existing manual (live) entries
        print(f"[OK] Loaded {len(HISTORICAL_FF)} historical FF rows: {inserted} inserted, {updated} updated")

        # Show summary by month
        rows = await conn.fetch("""
            SELECT date_trunc('month', date)::date AS month,
                   COUNT(*) AS n,
                   SUM(net_idr_billions) AS total_net,
                   MIN(net_idr_billions) AS min_net,
                   MAX(net_idr_billions) AS max_net
            FROM foreign_flow_daily
            WHERE date >= '2025-08-01' AND date <= '2026-01-31'
            GROUP BY 1 ORDER BY 1
        """)
        print(f"\n{'Month':<10} {'Days':>5} {'Total net':>12} {'Min':>12} {'Max':>12}")
        print("-" * 55)
        for r in rows:
            print(f"{str(r['month'])[:7]:<10} {r['n']:>5} {r['total_net']:>12.1f} {r['min_net']:>12.1f} {r['max_net']:>12.1f}")

        total = await conn.fetchval("SELECT COUNT(*) FROM foreign_flow_daily")
        print(f"\nTotal FF rows in DB: {total}")
    finally:
        await conn.close()

asyncio.run(main())
